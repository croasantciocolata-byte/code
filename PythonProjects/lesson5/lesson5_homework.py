# Arithmetic operations
x = 10
y = 28
print(y // x)
print(type(y // x))

# Assignment operations
x = 10
y = 28
x += y
print(x)
print(type(x))

# Comparison operations
x = 10
y = 28
print(y != x)
print(type(y != x))

# Logical operations
x = 10
y = 28
print(x >= y or y <= x)
print(type(x >= y or y <= x))

# Identity operations
x = {"pantera", "tiger"}
a = {"love", "happy"}
print(x is not a)
print(type(x is not a))

# Membership operations
x = {"pantera", "tiger"}
print("pantera" in x)
print(type("pantera" in x))